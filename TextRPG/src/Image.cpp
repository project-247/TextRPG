// 테두리 네모 창 출력
void Image::RenderSystemUI() {
    std::string line(SCREEN_X - 2, '-');
    // 상단
    moveCursor(0, 0);
    std::cout << "┌" << line << "┐";
    // 좌우
    for (int i = 1; i < SCREEN_Y - 1; ++i) {
        moveCursor(0, i);
        std::cout << R"(│)";
        moveCursor(SCREEN_X - 1, i);
        std::cout << R"(│)";
    }
    // 하단
    moveCursor(0, SCREEN_Y - 1);
    std::cout << "└" << line << "┘";
    moveCursor(0, SCREEN_Y + 2);
}

